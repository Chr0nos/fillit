#include "fillit.h"

int		tetro_width(t_element *t, int line)
{
	char	*s;
	int		p;

	s = t->data[line];
	while ((*s) && (*s == '.'))
		s++;
	p = 0;
	while ((s[p] != '\0') && (s[p] != '.'))
		p++;
	return (p);
}
